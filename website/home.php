<h1>Image Viewer</h1>
<p>
This is the home page of Image Viewer. 
Image Viewer is the most feature rich open source dhtml image viewer. 
Image Viewer uses javascript and jquery. 
</p>

<h3>Image Viewer Description</h3>
<p>
Image Viewer is  
It is written in javascript with jquery and jqueryui but supports frameworks that use the $ character. 
Originaly written for a commercial image gallery application. 
Later published under an opensource license. 
It supports large image collections by optionaly offering a lazy loading callback. 
Supports all common browsers. 
According to the license if you want to use it in a commercial application you must ask 
for permission from the company that developed it either menta.de or vbmscode.com.
</p>

